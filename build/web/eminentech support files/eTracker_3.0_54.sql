CREATE TABLE  "MAIL_CAMPAIGN" 
   (	"CAMPAIGN_ID" NUMBER NOT NULL ENABLE, 
	"MAILID" VARCHAR2(100) NOT NULL ENABLE, 
	"SUBJECT" VARCHAR2(200) NOT NULL ENABLE, 
	"MAILDATE" DATE NOT NULL ENABLE, 
	"SENTBY" NUMBER, 
        "STATUS" VARCHAR2(10),
	 CONSTRAINT "MAIL_CAMPAIGN_PK" PRIMARY KEY ("CAMPAIGN_ID") ENABLE
   )
/

CREATE OR REPLACE TRIGGER  "BI_MAIL_CAMPAIGN" 
  before insert on "MAIL_CAMPAIGN"               
  for each row  
begin   
    select "MAIL_CAMPAIGN_SEQ".nextval into :NEW.CAMPAIGN_ID from dual; 
end; 
/

