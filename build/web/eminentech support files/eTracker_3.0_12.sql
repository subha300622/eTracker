CREATE TABLE  "APM_WRM_DAY" 
   (	"WRM_ID" NUMBER NOT NULL ENABLE, 
	"PID" NUMBER NOT NULL ENABLE, 
	"WRM_DAY" NUMBER NOT NULL ENABLE, 
	 CONSTRAINT "APM_WRM_DAY_PK" PRIMARY KEY ("WRM_ID") ENABLE, 
	 CONSTRAINT "APM_WRM_DAY_CON" UNIQUE ("PID") ENABLE
   )
/
CREATE SEQUENCE   "APM_WRM_DAY_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 NOCACHE  NOORDER  NOCYCLE
/
CREATE OR REPLACE TRIGGER  "BI_APM_WRM_DAY" 
  before insert on "APM_WRM_DAY"               
  for each row  
begin   
    select "APM_WRM_DAY_SEQ".nextval into :NEW.WRM_ID from dual; 
end;
/
