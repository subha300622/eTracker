CREATE TABLE APM_SAP_CRID(
SNO NUMBER NOT NULL,
ISSUEID VARCHAR2(15) NOT NULL,
CRID VARCHAR2(15) NOT NULL,
DESCRIPTION VARCHAR2(75),
CONSTRAINT SN0_PK PRIMARY KEY("SNO") ENABLE,
CONSTRAINT ISSUEID_FK FOREIGN KEY("ISSUEID") REFERENCES ISSUE("ISSUEID") ON DELETE CASCADE ENABLE
);
CREATE SEQUENCE   "CRID_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 NOCACHE  NOORDER  NOCYCLE