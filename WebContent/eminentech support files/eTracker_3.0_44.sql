	"STATUS" NUMBER(19,0) NOT NULL ENABLE, 
	"CREATEDON" TIMESTAMP (6) NOT NULL ENABLE, 
	"MODIFIEDON" TIMESTAMP (6) NOT NULL ENABLE, 
	 CONSTRAINT "TIMESHEET_MAINTANANCE_PK" PRIMARY KEY ("ID") ENABLE, 
	 CONSTRAINT "TIMESHEET_MAINTANANCE_UK1" UNIQUE ("STATUS") ENABLE, 
	 CONSTRAINT "TIMESHEET_MAINTANANCE_CON" FOREIGN KEY ("USERID")
	  REFERENCES  "USERS" ("USERID") ENABLE
