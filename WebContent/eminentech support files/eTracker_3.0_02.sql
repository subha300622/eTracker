CREATE TABLE  "CRM_COUNTRY" 
   (	"COUNTRYID" NUMBER, 
	"COUNTRY" NUMBER NOT NULL ENABLE, 
	"CREATEDON" TIMESTAMP (6) NOT NULL ENABLE, 
	"MODIFIEDON" TIMESTAMP (6) NOT NULL ENABLE, 
        "CREATEDBY" NUMBER NOT NULL ENABLE,
        "MODIFIEDBY" NUMBER NOT NULL ENABLE,
	 CONSTRAINT "CRM_COUNTRY_PK" PRIMARY KEY ("COUNTRYID") ENABLE, 
	 CONSTRAINT "CRM_COUNTRY_UK" UNIQUE ("COUNTRY") ENABLE
   )/

CREATE SEQUENCE   "CRM_COUNTRY_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 NOCACHE  NOORDER  NOCYCLE
/
CREATE OR REPLACE TRIGGER  "TRG_CRM_COUNTRY" 
  before insert on "CRM_COUNTRY"               
  for each row  
begin   
    select "CRM_COUNTRY_SEQ".nextval into :NEW.ID from dual; 
end; 
/
ALTER TRIGGER  "TRG_CRM_COUNTRY" ENABLE
/

